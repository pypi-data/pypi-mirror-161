def sum(x,y):
    return x+y

def add_one(number):
    return number + 1

def average(x,y):
    return (x+y)/2

def power(x,y):
    return x**y

def add(num1,num2):
    return num1+num2

def subtract(num1,num2):
    return num1-num2

def multiply(num1,num2):
    return num1*num2

def divide(num1,num2):
    return num1/num2
